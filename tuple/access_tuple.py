this_tuple = ("apple", "banana", "cherry")

print(this_tuple[0])  # Output: apple
print(this_tuple[1])  # Output: banana
print(this_tuple[2])  # Output: cherry

print(this_tuple[-1])  # Output: cherry
print(this_tuple[-2])  # Output: banana
print(this_tuple[-3])  # Output: apple

print(this_tuple[1:])  # Output: ('banana', 'cherry')
